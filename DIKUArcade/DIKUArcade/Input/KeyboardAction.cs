namespace DIKUArcade.Input {

/// <summary>
/// Defines the possible actions associated with keyboard inputs.
/// </summary>
public enum KeyboardAction {
    /// <summary>
    /// Represents the action of pressing a key.
    /// </summary>
    KeyPress,

    /// <summary>
    /// Represents the action of releasing a key.
    /// </summary>
    KeyRelease
}
}
